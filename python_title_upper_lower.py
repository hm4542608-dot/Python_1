#Formating strings in python
#1.title() for title case
student1_name = "hassaan mahmood"
print(student1_name.title())
#2.upper() for upper case
student2_name = "hayyaan mahmood"
print(student2_name.upper())
#3.lower() for lower case
student3_name = "HABBAAN MAHMOOD"
print(student3_name.lower())